package ru.itsphere.subscription.domain;

/**
 * Represents abstraction for client's subscription
 */
public class Subscription {
    private long id;
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
